var percentage = 35;
if (percentage >= 80 && percentage < 100) {
    console.log("A+ Grade");
}
else if (percentage >= 70 && percentage <= 79.99) {
    console.log("A Garde");
}
else if (percentage >= 60 && percentage <= 69.99) {
    console.log("B Grade");
}
else if (percentage >= 50 && percentage <= 59.99) {
    console.log("C Grade");
}
else {
    console.log("Fail");
}
